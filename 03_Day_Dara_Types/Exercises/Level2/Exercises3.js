
let largo = prompt("Ingrese el largo del rectángulo:");


let ancho = prompt("Ingrese el ancho del rectángulo:");


largo = parseFloat(largo);
ancho = parseFloat(ancho);


let areaa = largo * ancho;

let perimetroo = 2 * (largo + ancho);


alert("El área del rectángulo es: " + areaa);
alert("El perímetro del rectángulo es: " + perimetroo);
